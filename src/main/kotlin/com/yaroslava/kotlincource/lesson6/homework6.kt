fun recommendLook(temperature: Double) : String {
    return when {
        temperature < -30 || >= 35 -> "Не выходите из дома"
        temperature > -30 && < 0 -> "Куртка и шапка"
        temperature <= 15 && < 35 -> "Ветровка"
        else -> "Футболка и шорты"
    }
}

