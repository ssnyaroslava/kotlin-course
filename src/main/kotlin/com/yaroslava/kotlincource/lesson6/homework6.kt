fun findSeason(month: Int): String {
   return when (month) {
        12, 1, 2 -> "Зима"
        3, 4, 5 -> "Весна"
        6, 7, 8 -> "Лето"
        9, 10, 11 -> "Осень"
        else -> "Некорректный номер месяца"
    }
}

fun dogToHumanYears(dogAge: Double): Double {
    return if (dogAge <= 2) {
        dogAge * 10.5
    } else {
        2 * 10.5 + (dogAge - 2) * 4
    }
}


fun chooseTransport(distance: Double): String {
    return if (distance in 0..1) {
        "пешком"
        } else if
                (distance in 2..5) {
            "велосипед"
        }
        else {
            "автотранспорт"
        }
    }


fun countBonus(purchaseAmount: Double): Int {
    return if (purchaseAmount <= 1000) {
        (purchaseAmount / 100 * 2).toInt()
    } else {
        (purchaseAmount / 100 * 5).toInt()
    }
}

